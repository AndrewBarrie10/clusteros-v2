#!/usr/bin/env node
/**
 * Build script for the Diagnostic Journey app.
 *
 * Compiles diagnostic-journey.jsx (React + JSX source) into
 * diagnostic-journey.app.js (plain browser JS). This lets the page ship
 * pre-compiled code instead of downloading @babel/standalone (~3 MB) and
 * transpiling ~400 KB of JSX in the browser on every visit.
 *
 * Requirements (one-time):
 *   npm install --no-save @babel/core @babel/preset-react
 *
 * Run:
 *   node scripts/build-diagnostic-journey.js
 *
 * The compiled output is committed to the repo and served directly by Vercel.
 */
const fs = require('fs');
const path = require('path');

let babel;
try {
  babel = require('@babel/core');
} catch (e) {
  console.error('Missing build dependency. Run:\n  npm install --no-save @babel/core @babel/preset-react');
  process.exit(1);
}

const root = path.resolve(__dirname, '..');
const SRC = path.join(root, 'diagnostic-journey.jsx');
const OUT = path.join(root, 'diagnostic-journey.app.js');

const source = fs.readFileSync(SRC, 'utf8');

const { code } = babel.transformSync(source, {
  filename: 'diagnostic-journey.jsx',
  // Classic runtime keeps JSX as React.createElement(...) so the compiled file
  // needs only the global `React` (no module imports / no bundler).
  presets: [['@babel/preset-react', { runtime: 'classic' }]],
  comments: false,
  compact: false,
});

const banner =
  '/* AUTO-GENERATED from diagnostic-journey.jsx by scripts/build-diagnostic-journey.js.\n' +
  '   Do not edit this file directly — edit the .jsx source and rerun the build. */\n';

// Wrap in an IIFE to preserve the original module scoping. The source ran as
// <script type="text/babel" data-type="module">, so no top-level binding should
// leak into the global scope.
const output = banner + ';(function () {\n' + code + '\n})();\n';

fs.writeFileSync(OUT, output);
console.log('Wrote ' + path.relative(root, OUT) + ' (' + fs.statSync(OUT).size + ' bytes)');
